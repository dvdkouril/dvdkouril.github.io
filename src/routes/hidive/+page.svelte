<h1>3D genomics @ HIDIVE</h1>
<p>
My goal in Nil Gehlenborg's HIDIVE Lab is to improve visualization tools for 3D chromatin structural data. Many tools have been developed for genomics and investigation of chromatin's spatial structure is important. However, the tools currently used in day-to-day practice are mostly from proteomics.
</p>

<p>
Projects:
</p>
<ul>
	<li>chromospace: javascript library for visualization of 3D chromatin structures</li>
	<li>chromospyce: anywidget-powered computational notebook version of chromospace</li>
	<li>hyperwindows: visualization technique for navigating complex 3D structures via a hierarchical layout and interactions</li>
</ul>

<p>Prototypes:</p>
<ul>
	<li>hierarchical-selections prototype: hierarchical selections in a 3D chromatin model, visualization via a fixed tree-like layout. (code)</li>
	<li>chromatin-n-out (aka hyperwindows) prototype: hierarchical selections in a 3D chromatin model, visualization via a fluid physics-based layout. (code)</li>
	<li>really-3d-genome: survey paper about 3D genome modeling and visualization (wip)</li>
</ul>

